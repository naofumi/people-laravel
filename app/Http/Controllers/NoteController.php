    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create(Request $request)
    {
        $notable = Note::notableFindSafe(
            notableId: $request->query('notable_id'),
            notableType: $request->query('notable_type')
        );

        $note = new Note();

        return view('notes.create', ['note' => $note, 'notable' => $notable]);
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \App\Http\Requests\StoreNoteRequest  $request
     * @return \Illuminate\Http\Response
     */
    public function store(StoreNoteRequest $request)
    {
        // Will replace with Authenticated user later
        $user = User::first();
        $note = Note::create(array_merge(
            $request->validated(),
            ['noter_id' => $user->id, 'noter_type' => get_class($user)]
        ));

        return redirect(route('people.show', $note->notable->id))
                 ->withSuccess('Note was successfully created.');
    }

